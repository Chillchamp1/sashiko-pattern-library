// ── Polyline engine (Tsuzuki Yamagata) ────────────────────────────────────
// Geometry extracted programmatically from the book diagram (tools/pattern_extractor.py):
// the pattern is the union of straight running-stitch lines at four slopes — shallow
// ±1/2 (flow left→right) and steep ±2 (flow top→bottom) — crossing one another to form
// the mountain mesh. Each line is stitched continuously; +slope and −slope are mirror
// images, shown in the two phase shades. Encoded on a HALF-grid lattice as a unit cell
// + generators. Verified to reproduce the extracted edge set 100% (0 false positives).
// Pass 1 = horizontal (shallow, green H shades), Pass 2 = vertical (steep, blue V shades).
const PL_NHU=28;                       // TY: half-grid units across the canvas (= 14 "grid squares")
let PL_N=PL_NHU;                        // lattice units across the canvas (set per pattern)
let PL_HU=(SIZE-2*PAD)/PL_N;            // px per lattice unit (recomputed in loadPattern)
let PL_guideStep=2;                     // guide grid drawn every N units (= one "grid square")
let PL_path=[], PL_fronts=[], PL_shCount=0;
// Generic multi-pass descriptor: [{start, label, glyph, col}] (col = PHASE_COLORS key).
// TY has 2 passes; Asanoha has 4. Drives the jump bar and the info pill.
let PL_passes=[];

// Unit cell: [I, J, dx, dy] edges in half-units; lattice generators G1, G2.
const TY_CELL=[
  [0,0,1,2],[0,0,2,-1],  [1,2,1,-2],[1,2,1,2],  [2,0,1,2],
  [3,2,1,-2],[3,2,1,2],  [4,0,1,-2],[4,0,2,1],  [4,2,2,-1],[4,2,2,1],
  [6,0,1,-2],  [6,1,2,-1],[6,1,2,1],  [6,3,2,-1],[6,3,2,1],
];
const TY_G1=[4,4], TY_G2=[8,0];

function genTYedges(NHU){
  const edges=[], seen=new Set();
  for(let a=-2;a<=NHU/4+2;a++) for(let b=-(NHU>>3)-2;b<=(NHU>>3)+2;b++){
    const ox=a*TY_G1[0]+b*TY_G2[0], oy=a*TY_G1[1]+b*TY_G2[1];
    for(const [I,J,dx,dy] of TY_CELL){
      let x1=I+ox, y1=J+oy, x2=x1+dx, y2=y1+dy;
      if(x1<0||x1>NHU||x2<0||x2>NHU||y1<0||y1>NHU||y2<0||y2>NHU) continue;
      let A=[x1,y1], B=[x2,y2];
      if(A[0]>B[0]||(A[0]===B[0]&&A[1]>B[1])){const t=A;A=B;B=t;}
      const key=A[0]+','+A[1]+','+B[0]+','+B[1];
      if(seen.has(key)) continue; seen.add(key);
      edges.push({x1:A[0],y1:A[1],x2:B[0],y2:B[1],fam:Math.abs(B[0]-A[0])===2?'sh':'st'});
    }
  }
  return edges;
}

// Trace continuous zigzag lines (running stitch), optimised for FEW direction changes
// and SHORT jumps (see routing rules in CLAUDE.md). At every vertex the incident edges
// are PAIRED by collinearity, so a stroke goes straight through crossings and only turns
// at genuine peaks/valleys → longest possible arms. tracePaired returns those strokes;
// orderNN then sequences them nearest-neighbour so the needle barely jumps between strokes.
function tyGcd(a,b){while(b){const t=b;b=a%b;a=t;}return a||1;}
function tyUnit(d){const g=tyGcd(Math.abs(d[0]),Math.abs(d[1]));return[d[0]/g,d[1]/g];}

function tracePaired(edges){
  const pk=p=>p[0]+','+p[1];
  const ek=(a,b)=>(a[0]<b[0]||(a[0]===b[0]&&a[1]<b[1]))?pk(a)+'|'+pk(b):pk(b)+'|'+pk(a);
  const adj=new Map(), rem=new Set();
  const addn=(a,b)=>{const k=pk(a);if(!adj.has(k))adj.set(k,{v:a,nb:[]});adj.get(k).nb.push(b);};
  for(const e of edges){const a=[e.x1,e.y1],b=[e.x2,e.y2];addn(a,b);addn(b,a);rem.add(ek(a,b));}
  const pair=new Map();                                   // pk(v) → Map(pk(nbr)→nbr)
  for(const {v,nb} of adj.values()){
    const used=nb.map(()=>false), dirs=nb.map(n=>tyUnit([n[0]-v[0],n[1]-v[1]])), pm=new Map();
    pair.set(pk(v),pm);
    for(let i=0;i<nb.length;i++){
      if(used[i])continue;
      let best=-1;
      for(let j=0;j<nb.length;j++){if(used[j]||j===i)continue;if(dirs[j][0]===-dirs[i][0]&&dirs[j][1]===-dirs[i][1]){best=j;break;}}
      if(best<0)for(let j=0;j<nb.length;j++){if(!used[j]&&j!==i){best=j;break;}}
      if(best>=0){used[i]=used[best]=true;pm.set(pk(nb[i]),nb[best]);pm.set(pk(nb[best]),nb[i]);}
      else used[i]=true;
    }
  }
  const chains=[];
  for(const e of edges){
    const A0=[e.x1,e.y1],B0=[e.x2,e.y2];
    if(!rem.has(ek(A0,B0)))continue;
    const poly=[A0,B0]; rem.delete(ek(A0,B0)); let u=A0,v=B0;
    while(true){const pm=pair.get(pk(v));const w=pm&&pm.get(pk(u));if(!w||!rem.has(ek(v,w)))break;rem.delete(ek(v,w));poly.push(w);u=v;v=w;}
    u=B0;v=A0;
    while(true){const pm=pair.get(pk(v));const w=pm&&pm.get(pk(u));if(!w||!rem.has(ek(v,w)))break;rem.delete(ek(v,w));poly.unshift(w);u=v;v=w;}
    chains.push(poly);
  }
  return chains;
}

// Colour = translation-equivalence class. Two paths get the same shade iff one maps
// onto (part of) the other by a pattern-symmetry translation (lattice generated by
// (4,4) & (8,0) in half-units) — so a path that's merely shifted, INCLUDING a border-
// clipped piece whose uncut part reappears shifted elsewhere, counts as identical.
// A path and its mirror (half-period shift, NOT a lattice vector) stay distinct → 2 shades.
const TY_LAT=(()=>{const L=[];for(let k1=-8;k1<=8;k1++)for(let k2=-8;k2<=8;k2++)L.push([4*k1+8*k2,4*k1]);return L;})();
function edgeKeyArr(poly){
  const r=[];
  for(let k=0;k<poly.length-1;k++){let a=poly[k],b=poly[k+1];
    if(a[0]>b[0]||(a[0]===b[0]&&a[1]>b[1])){const t=a;a=b;b=t;}
    r.push([a[0],a[1],b[0],b[1]]);}
  return r;
}
function latContainsMin(bigSet,smallArr){  // min |t|² s.t. (smallArr+t) ⊆ bigSet, or Infinity
  let best=Infinity;
  for(const t of TY_LAT){
    let ok=true;
    for(const e of smallArr){
      if(!bigSet.has((e[0]+t[0])+','+(e[1]+t[1])+','+(e[2]+t[0])+','+(e[3]+t[1]))){ok=false;break;}
    }
    if(ok){const d=t[0]*t[0]+t[1]*t[1];if(d<best)best=d;}
  }
  return best;
}
function classifyTranslation(chains){
  // Assign translation-equivalence class by BEST (smallest |t|) match, not first match.
  // A border-clipped piece accidentally fits inside both reps; the correct one needs a
  // smaller translation because the piece geometrically continues from that class.
  const order=[...chains.keys()].sort((a,b)=>chains[b].length-chains[a].length);
  const reps=[], cls=new Array(chains.length);
  for(const i of order){
    const arr=edgeKeyArr(chains[i]), set=new Set(arr.map(e=>e.join(',')));
    let bestRep=-1, bestDist=Infinity;
    for(let r=0;r<reps.length;r++){
      const d=Math.min(latContainsMin(reps[r].set,arr),latContainsMin(set,reps[r].arr));
      if(d<bestDist){bestDist=d;bestRep=r;}
    }
    if(bestRep<0){reps.push({set,arr});bestRep=reps.length-1;}
    cls[i]=bestRep;
  }
  return cls;
}

function orderNN(items){                          // nearest-neighbour stroke order; carries colour
  const rem=items.map((_,i)=>i), seq=[]; let cur=null;
  while(rem.length){
    let best=-1,bd=Infinity,brev=false;
    for(const i of rem)for(const rev of [false,true]){
      const p=items[i].poly, st=rev?p[p.length-1]:p[0];
      const d=cur?((cur[0]-st[0])**2+(cur[1]-st[1])**2):0;
      if(d<bd){bd=d;best=i;brev=rev;}
    }
    const it=items[best], poly=brev?it.poly.slice().reverse():it.poly;
    seq.push({poly,col:it.col}); cur=poly[poly.length-1]; rem.splice(rem.indexOf(best),1);
  }
  return seq;
}

function buildTsuzukiYamagata(NHU){
  const edges=genTYedges(NHU);
  const family=(f,pal)=>{
    const chains=tracePaired(edges.filter(e=>Math.abs(e.x2-e.x1)===f));
    // Geometric invariant: band-coord % 8 === 4 → class 0 (light), === 0 → class 1 (dark).
    // Bypasses lattice heuristic which misclassifies clipped border chains.
    // H (steep, f=1): band = start y.  V (flat, f=2): band = start x.
    const cls=f===1
      ? chains.map(c=>(c[0][1]%8===4)?0:1)
      : chains.map(c=>(c[0][0]%8===4)?0:1);
    const items=chains.map((poly,i)=>({poly,col:pal[cls[i]%pal.length]}));
    // V family: sort by x-band so x=0 border chains come before full chains,
    // x=28 border chains after — prevents NN leaving one border stranded far from the other.
    if(f===2) items.sort((a,b)=>a.poly[0][0]-b.poly[0][0]);
    return orderNN(items);
  };
  const H=family(1,PHASE_COLORS.H);              // horizontal march (green)
  const V=family(2,PHASE_COLORS.V);              // vertical march (blue, 90° rot)
  const path=[], fronts=[];
  const emit=items=>items.forEach(it=>{for(let k=0;k<it.poly.length-1;k++){
    fronts.push(path.length);
    path.push({type:'front',x1:it.poly[k][0],y1:it.poly[k][1],x2:it.poly[k+1][0],y2:it.poly[k+1][1],col:it.col});
  }});
  const passes=[{start:0,label:'Horizontal ranges (shallow)',glyph:'↗',col:'H'}];
  emit(H);
  passes.push({start:fronts.length,label:'Vertical ranges (steep)',glyph:'↕',col:'V'});
  emit(V);
  return {path,fronts,passes,shCount:passes[1].start};
}

function plPx(coord){return PAD+coord*PL_HU;}
function drawPLGuide(){
  ctx.strokeStyle='rgba(220,235,255,0.07)';ctx.lineWidth=1;ctx.setLineDash([]);
  const e=PAD+PL_N*PL_HU;
  for(let i=0;i<=PL_N;i+=PL_guideStep){
    const p=PAD+i*PL_HU;
    ctx.beginPath();ctx.moveTo(p,PAD);ctx.lineTo(p,e);ctx.stroke();
    ctx.beginPath();ctx.moveTo(PAD,p);ctx.lineTo(e,p);ctx.stroke();
  }
}
function drawPLFront(seg){
  const x1=plPx(seg.x1),y1=plPx(seg.y1),x2=plPx(seg.x2),y2=plPx(seg.y2);
  ctx.lineCap='round';ctx.setLineDash([]);
  ctx.strokeStyle='rgba(0,8,20,0.45)';ctx.lineWidth=3.6;
  ctx.beginPath();ctx.moveTo(x1,y1+0.7);ctx.lineTo(x2,y2+0.7);ctx.stroke();
  ctx.strokeStyle=seg.col;ctx.lineWidth=2.3;
  ctx.beginPath();ctx.moveTo(x1,y1);ctx.lineTo(x2,y2);ctx.stroke();
}
function renderPolyline(st){
  ctx.setLineDash([]);drawFabric();drawPLGuide();
  const pos=(st===0)?-1:PL_fronts[st-1];
  for(let i=0;i<=pos;i++) drawPLFront(PL_path[i]);
  if(pos>=0){const s=PL_path[pos];drawNeedleAt(plPx(s.x2),plPx(s.y2),s.col);}
  updateInfoPL(st);
}
function updateInfoPL(st){
  const el=document.getElementById('info');
  if(st===0){setIdleInfo();markJump(-1);return;}
  el.classList.remove('idle');el.onclick=null;
  const total=PL_fronts.length, np=PL_passes.length;
  if(st>=total){el.innerHTML='<span class="muted">complete ✓</span>';markJump(np-1);return;}
  let pi=0; for(let k=0;k<np;k++) if((st-1)>=PL_passes[k].start) pi=k;
  const p=PL_passes[pi], col=PHASE_COLORS[p.col][0];
  const pill=`<span class="pill" style="background:${hexA(col,.16)};color:${col}"><span class="dot" style="background:${col}"></span>pass ${pi+1}/${np} · ${p.label} ${p.glyph}</span>`;
  el.innerHTML=`stitch <b>${st}</b><span class="muted">/${total}</span> &nbsp;${pill}`;
  markJump(pi);
}
function renderPLThumb(canvas,pat){
  const TDPR=Math.min(window.devicePixelRatio||1,2);
  const isAsa=pat&&pat.engine==='asanoha';
  const TPAD=3,TS=64;
  canvas.width=TS*TDPR;canvas.height=TS*TDPR;
  canvas.style.width='64px';canvas.style.height='64px';
  const tc=canvas.getContext('2d');tc.scale(TDPR,TDPR);
  tc.fillStyle='#1a3a5c';tc.fillRect(0,0,TS,TS);
  tc.lineCap='round';
  const TN=PL_NHU, TG=(TS-2*TPAD)/TN;
  const{path}=buildTsuzukiYamagata(TN);
  tc.lineWidth=1;
  for(const seg of path){
    tc.strokeStyle=seg.col;
    tc.beginPath();tc.moveTo(TPAD+seg.x1*TG,TPAD+seg.y1*TG);tc.lineTo(TPAD+seg.x2*TG,TPAD+seg.y2*TG);tc.stroke();
  }
}

